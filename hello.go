package main // This tells the Go compiler that this is the main package and it can be executed directly.

import "fmt" // Importing the fmt package to use its functions for formatted Input and Output.

// The main function is the entry point of the program.

func main() { // Start of the main function
	fmt.Println(2+3, "My name is Nilesh") // Print the result of 2 + 3 and a greeting message to the console. fmt.Println is used for formatted output.
}

// End of the main function and to run the program, use the command: go run hello.go
